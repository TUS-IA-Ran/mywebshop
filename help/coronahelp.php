<h1>   <font color=#FF97C2>新型コロナウイルスと注文・配送に関するよくある質問について</h1></font>
新型コロナウイルスの影響下においてもRAN  SHOPで継続してお買い物いただけます。<br>
なお、お届けにかかる時間が通常より長くなる場合があります。予めご了承ください。<br>
<hr><br>
 <font color=#FF0000>新型コロナウイルス流行期間限定で下記のサービスを実施しております。<br>ぜひこの際にお買い求めくださいませ。<br></font>
 <img src='../img/マスク.png' width='60%'><br><br><br><br>

 <font color=#FF0000>Q1．現在RAN  SHOPでお買い物はできますか？また、現在もRAN  SHOPは配送を行っていますか？<br></font>
A.はい、現在もRAN  SHOPではご注文を承っており、配送を行っています。<br>
ただし、お届けまでに通常より時間がかかる場合がございます。<br>
なお、お届け先の住所には閉鎖や休業の可能性がある施設（学校など）の住所を使用しないようご注意ください。<br><br><br><br>

 <font color=#FF0000>Q2.RAN  SHOPの会員かどうかに関わらず、お届けに通常より時間がかかっているのはなぜですか？<br></font>
A.新型コロナウイルス感染症の拡大により、オンラインでお買い物をするお客様が増えています。<br>
そのため、通常よりもお届けまでにお時間をいただく場合があります。<br><br><br><br>

<font color=#FF0000>Q3.返品はできますか？<br></font>
はい、RAN  SHOPでは引き続き返品を受け付けています。<br>
返品に関しての詳しい情報は<a href='cancelhelp.php'>こちら</a>から再度ご確認くださいますよう、お願いいたします。<br><br><br><br>

<font color=#FF0000>Q4.商品の受け取りは安全ですか？<br></font>
RAN  SHOPでは、お届けの際は手渡しを控えるだけなく、手洗いや手の消毒、配送用車両や携帯電話といった配送ツールの<br>
消毒も欠かさず行っています。<br><br><br><br>

<font color=#FF0000>Q5.商品の受け取り時に、ドライバーとの接触を軽減することはできますか？<br></font>
はい、RAN  SHOPでは受け取り時に手渡しではなく、置き配サービスをご利用いただくことも可能です<br>
置き配は、「玄関」だけでなく、「宅配ボックス」や「ガスメーターボックス」、「自転車のかご」、<br>
「車庫」、「建物内受付・管理人」などからお選びいただけます。<br>





